            cs.setString(3,"FES");
            cs.setString(4,"11/11/2011");
            cs.setString(5,cin);
            cs.setString(6,"phone");
            cs.setString(7, email);
            cs.setString(8, cin);
            cs.setInt(9, niveau);
        cs.registerOutParameter(10, Types.NUMERIC);
        cs.execute();
        System.out.println("Result = " + cs.getObject(10));
        this.loaddatatotable();
        cs.close(); 
        this.loaddatatotable();
