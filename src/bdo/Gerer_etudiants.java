    public  void loaddatatotable() throws SQLException, SQLException{
    if(this.loaddata()!=null){
        DefaultTableModel model=(DefaultTableModel) this.tableetudiants.getModel();
        model.setRowCount(0);
        Object[] row=new Object[6];
        ArrayList<Etudiant> etudiants=this.loaddata();
        for(int i=0;i<this.loaddata().size();i++){
        row[1]=etudiants.get(i).getNom();
        row[2]=etudiants.get(i).getPrenom();
        row[0]=etudiants.get(i).getCne();
        row[3]=etudiants.get(i).getEmail();
        row[4]=etudiants.get(i).getNiveau();
        row[5]=etudiants.get(i).getFiliere();

        model.addRow(row);
        }
    }
    else{
          DefaultTableModel model=(DefaultTableModel)  tableetudiants.getModel();
          model.setRowCount(0);
            return;

    }
    }
    
    
    
    


