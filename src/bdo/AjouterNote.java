    private void Reser1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Reser1MouseClicked
        File file=new File(Path.getText());
        List<String> lignes=new ArrayList<String>();
        try{
            Scanner scanner=new Scanner(file);
            while(scanner.hasNext()){
            lignes.add(scanner.next());
            
            
            }
            this.prepareNote(lignes);
        }
        catch(FileNotFoundException e ){
        e.printStackTrace();
        } catch (SQLException ex) {
            Logger.getLogger(AjouterNote.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Reser1MouseClicked

    
    public void prepareNote(List<String> lignes) throws SQLException{
     for(int i=0;i<lignes.size();i++){
        String plsql ="insert into  notes values(?,?,(select ref(e) from etudiants e where e.cne=?),(select ref(e) from elements e where e.nom=?))";
        CallableStatement cs;   
        String ligne[]=new String[10];
        ligne=lignes.get(i).split(",");
            try {
                cs = OracleCon.getInstance().getConnexion().prepareCall(plsql);
                cs.setString(1,ligne[0]);
                cs.setString(2,ligne[1]);
                cs.setString(3, ligne[2]);
                cs.setString(4,ligne[3]);
                cs.execute();
            }
             catch (SQLException ex) {
                Logger.getLogger(InsertionEtudiant.class.getName()).log(Level.SEVERE, null, ex);
            }
            finally{
            }
     }
    }
      public void filieres() throws SQLException{
       
        String plsql ="select * from filieres";
        CallableStatement cs= OracleCon.getInstance().getConnexion().prepareCall(plsql);   
        cs.execute();
        ResultSet rs=(ResultSet) cs.getResultSet();
        while(rs.next()){
        this.filieres.addItem(rs.getString("nom"));
        }
         }
     public void modules(String f) throws SQLException{
       String plsql ="select * from modules where deref(ref_filiere).nom=?";
        CallableStatement cs= OracleCon.getInstance().getConnexion().prepareCall(plsql);   
        cs.setString(1, f);
        cs.execute();
        ResultSet rs=(ResultSet) cs.getResultSet();
        while(rs.next()){
        this.modules.addItem(rs.getString("nom"));
        }
     }
     public void elements(String m) throws SQLException{
       String plsql ="select * from elements where deref(refmodule).nom=?";
        CallableStatement cs= OracleCon.getInstance().getConnexion().prepareCall(plsql);   
        cs.setString(1, m);
        cs.execute();
        ResultSet rs=(ResultSet) cs.getResultSet();
        while(rs.next()){  
        this.elements.addItem(rs.getString("nom"));
        }
     }
     
     
    
    
    
